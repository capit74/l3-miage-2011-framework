    private void jButton_SupprActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SupprActionPerformed
            try {
                
                List selected = this.jList_Message.getSelectedValuesList();
                Iterator i = selected.iterator();

                while(i.hasNext()){
                    String messageSelected = (String) i.next();
                    Message msgSelected = this.selectedSujet.getMsgByNom(messageSelected);
                    this.ancetre.application().getForum().deleteMessage(msgSelected);
                }
                
                this.initJlistMsg();
                
            } catch (RemoteException ex) {
                Logger.getLogger(AdminGestionForum.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_jButton_SupprActionPerformed

