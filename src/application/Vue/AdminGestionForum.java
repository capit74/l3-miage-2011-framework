    private void initBoxTopic() {
        
        try {
            
            ArrayList<Categorie> arrayListCategorie = this.ancetre.application().getForum().getCatalogueCategorie();
                
            Iterator it = arrayListCategorie.iterator();
            int cpt = 0;
            DefaultComboBoxModel dcbm = new javax.swing.DefaultComboBoxModel();

                while (it.hasNext()){
                    Categorie categorie = (Categorie) it.next();
                    dcbm.addElement(categorie.getNom());
                    
                        if (cpt == 0)
                            selectedTopic = categorie;
                    
                    cpt++;
                }
            
            this.jComboBox_Topic.setModel(dcbm);
            
            initBoxSujet();
            
        } catch (RemoteException ex) {
            Logger.getLogger(AdminGestionProfil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void initBoxSujet() {
            
            ArrayList<Sujet> arrayListSujet = this.selectedTopic.getSujet();
                
            Iterator it = arrayListSujet.iterator();
            int cpt = 0;
            DefaultComboBoxModel dcbm = new javax.swing.DefaultComboBoxModel();

                while (it.hasNext()){
                    Sujet sujet = (Sujet) it.next();
                    dcbm.addElement(sujet.getNom());
                    
                        if (cpt == 0)
                            selectedSujet = sujet;
                    
                    cpt++;
                }
            
            this.jComboBox_Sujet.setModel(dcbm);
            
            if (selectedSujet != null)
            initJlistMsg();

    }
    
    private void initJlistMsg() {
        
            ArrayList<Message> arrayListMessage = this.selectedSujet.getMessage();
                
            Iterator it = arrayListMessage.iterator();
            int cpt = 0;
            DefaultListModel alm = new javax.swing.DefaultListModel();

                while (it.hasNext()){
                    Message message = (Message) it.next();
                    alm.add(cpt, message.getMessage());
                    cpt++;
                }
            
            this.jList_Message.setModel(alm);

    }
    
